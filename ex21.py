def add(a, b):
    print("ADDING %d + %d" % (a,b))
    return(a+b)

def subtract(a, b):
    print("SUBTRACTING %d - %d" % (a, b))
    return(a - b)

def multiply(a, b):
    print("MULTIPLYING %d * %d" (a, b))
    return(a * b)

def divide(a, b):
    print("DIVIDING %d / %d" % (a, b))
    return(a / b)

